/**
 * Сколько карточек пользователь может держать в проекте одновременно.
 * - После достижения лимита UI перестанет принимать новые фото.
 * - Используется в состоянии (`useProjectStore`) как `max`.
 * Типичные значения: 12, 16, 24, 36. Большее значение замедляет экспорт в PDF.
 */
export const MAX_CARDS = 16;

/**
 * Итоговая длина стороны одной карточки в пикселях при экспорте в PNG
 * или при встраивании в PDF-страницы.
 * - Это «истинный» размер рендера, которым пользуются экспорт и точный превью.
 * - Должно быть положительное целое (например, 1024, 1500, 2048, 2050).
 *   2048 — распространённый вариант (степень двойки); 2050 — тоже ок.
 * Компромисс: чем больше размер, тем четче картинка, но тяжелее файлы и дольше экспорт.
 */
export const CARD_EXPORT_SIZE = 2050;

/**
 * Размер точного превью (даунскейл от итогового размера экспорта).
 * - Используется в renderCardExactPreviewUrl(): сначала рендерим в CARD_EXPORT_SIZE,
 *   затем уменьшаем до этого значения для пиксель-точного превью.
 * - Держите значение <= CARD_EXPORT_SIZE, чтобы избежать апскейла.
 * Типичные значения: 512–1024.
 */
export const PREVIEW_EXACT_SIZE = 800;

/**
 * Размер «быстрого» превью для лёгких миниатюр в сетке, когда не нужен
 * точный путь экспорта (чистый «быстрый» canvas-рендер).
 * - Про скорость отклика в гриде; для финального экспорта не используется.
 * Типичные значения: 768–1024.
 */
export const PREVIEW_FAST_SIZE = 1024;

/**
 * Значение JPEG-качества (0..1) по умолчанию при встраивании растеризованных карточек в PDF.
 * - Применяется только если формат PDF — JPEG (меньший размер файла).
 * - PNG это значение игнорирует (lossless).
 * Рекомендации:
 *   0.70–0.80  → меньшие PDF, лёгкая компрессия
 *   0.82–0.88  → хороший баланс (по умолчанию)
 *   0.90–1.00  → тяжелые PDF, визуально почти без потерь
 */
export const PDF_JPEG_QUALITY = 0.80;
